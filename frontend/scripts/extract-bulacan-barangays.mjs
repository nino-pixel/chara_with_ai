/**
 * One-off: downloads PSGC-style JSON and writes src/data/bulacanBarangays.generated.ts
 * Run: node scripts/extract-bulacan-barangays.mjs
 */
import fs from 'fs'
import https from 'https'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '../src/data/bulacanBarangays.generated.ts')

const URL =
  'https://raw.githubusercontent.com/flores-jacob/philippine-regions-provinces-cities-municipalities-barangays/master/philippine_provinces_cities_municipalities_and_barangays_2019v2.json'

/** UI labels (must match bulacan.php / bulacanAddress.ts) → keys in JSON */
const MUNI_MAP = {
  Angat: 'ANGAT',
  Balagtas: 'BALAGTAS (BIGAA)',
  Baliuag: 'BALIUAG',
  Bocaue: 'BOCAUE',
  Bulakan: 'BULACAN',
  Bustos: 'BUSTOS',
  Calumpit: 'CALUMPIT',
  'Doña Remedios Trinidad': 'DOÑA REMEDIOS TRINIDAD',
  Guiguinto: 'GUIGUINTO',
  Hagonoy: 'HAGONOY',
  Malolos: 'MALOLOS CITY',
  Marilao: 'MARILAO',
  Meycauayan: 'MEYCAUAYAN CITY',
  Norzagaray: 'NORZAGARAY',
  Obando: 'OBANDO',
  Pandi: 'PANDI',
  Paombong: 'PAOMBONG',
  Plaridel: 'PLARIDEL',
  Pulilan: 'PULILAN',
  'San Ildefonso': 'SAN ILDEFONSO',
  'San Jose del Monte': 'SAN JOSE DEL MONTE CITY',
  'San Miguel': 'SAN MIGUEL',
  'San Rafael': 'SAN RAFAEL',
  'Santa Maria': 'SANTA MARIA',
}

function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode}`))
          return
        }
        const chunks = []
        res.on('data', (c) => chunks.push(c))
        res.on('end', () => resolve(JSON.parse(Buffer.concat(chunks).toString('utf8'))))
      })
      .on('error', reject)
  })
}

async function main() {
  console.log('Downloading…')
  const data = await fetchJson(URL)
  let bul = null
  for (const reg of Object.values(data)) {
    const pl = reg?.province_list
    if (pl && pl.BULACAN) {
      bul = pl.BULACAN
      break
    }
  }
  if (!bul?.municipality_list) {
    throw new Error('BULACAN not found in JSON')
  }
  const ml = bul.municipality_list

  const lines = []
  lines.push('/**')
  lines.push(' * Barangays per municipality/city in Bulacan.')
  lines.push(' * Generated by scripts/extract-bulacan-barangays.mjs — do not edit by hand.')
  lines.push(' */')
  lines.push('')
  lines.push('export const BARANGAYS_BY_MUNICIPALITY = {')

  for (const uiName of Object.keys(MUNI_MAP)) {
    const jsonKey = MUNI_MAP[uiName]
    const entry = ml[jsonKey]
    if (!entry?.barangay_list?.length) {
      throw new Error(`Missing barangays for ${uiName} (JSON key: ${jsonKey})`)
    }
    const sorted = [...entry.barangay_list].sort((a, b) => a.localeCompare(b, 'en', { sensitivity: 'base' }))
    const escaped = sorted.map((b) => JSON.stringify(b))
    lines.push(`  ${JSON.stringify(uiName)}: [${escaped.join(', ')}],`)
  }

  lines.push('} as const')
  lines.push('')
  lines.push('export type BulacanMunicipality = keyof typeof BARANGAYS_BY_MUNICIPALITY')
  lines.push('')

  fs.writeFileSync(OUT, lines.join('\n'), 'utf8')
  console.log('Wrote', OUT)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
